#Arithemtic operators
small = 10
big = 20

print(small / big)
print(small % big) #modulus (remainder)
print(small ** big) #exponent
print(small // big) #floor division (how many times denom goes into num)
print(big // small)
print((small + big) / big)

#Comparison operators
small = 10
big = 20

print(small > big)
print(small < big)
print(small >= big)
print(small <= big)
print(small != big)
