#for <variable> in <sequence>:

#Example for loop
shopping_list = ["milk", "eggs", "bread", "yoghurt", "meat", "flour"]
for i in shopping_list:
    print(i)
print("This loop individually prints each item of the list")
#For first iteration, 'i' variable has value "milk". Next iteration, it will have value "eggs"...
