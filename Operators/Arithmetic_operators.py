#Includes arithemtic operators that aren't obvious.
print("5%2 = ", 5%2, " - This is the modulus function, which gives the remainder of a division")

print("4**2 = ", 4**2, " - This is the exponent function, which enables you to do exponential calculations")

print("7//2 = ", 7//2, " - This is the floor division function, returning how many times the divisor goes into the numerator")
print("-5//2 = ", -5//2, " - If the true value is negative, the output will be rounded to the next highest negative integer")